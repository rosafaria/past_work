    public ManageGroup(UIClient client, String groupname) {
        initComponents();
        
        client.lastWindow().setVisible(true);
        this.setLocationRelativeTo(client.lastWindow());
        client.lastWindow().setVisible(false);
        this.setTitle(client.getUsername());
        
        this.client = client;
        this.groupname = groupname.trim();
        this.client.addWindow(this);
        
        ArrayList<Object> par= new ArrayList<Object>();
        par.add(groupname);
        par.add(false);
        Message permission = client.sendTCP(5,par);
        //check permissions and hide invalid buttons
        switch (permission.getResult()) {
            default:
                JOptionPane.showMessageDialog(null, "Error accessing group!", "Error",JOptionPane.ERROR_MESSAGE);
                leave();
                return;
            case -1:
                this.addUserBtn.setText("Join");
                this.leaveBtn.setVisible(false);
                perm = -1;
                break;
            case 1:
                perm = 1;
                break;
            case 2: //admin
                perm = 2;
                break;
            case 3:
                perm = 2;
                this.leaveBtn.setEnabled(false);
        }
        this.GroupName.setText(groupname);
        this.setVisible(true);
    }

