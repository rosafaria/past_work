    private void addUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserBtnActionPerformed
        if (this.addUserBtn.getText().equals("Join")) {
            ArrayList<Object> par = new ArrayList<Object>();
            par.add(groupname);
            par.add(client.getUsername());
            Message result = client.sendTCP(9, par);
            if (result.getResult()<0)
                JOptionPane.showMessageDialog(null, result.toString(), "Error",JOptionPane.ERROR_MESSAGE);
            else
                JOptionPane.showMessageDialog(null, result.toString(), "Success",JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            new SelectUser(client);
            this.setVisible(false);
        }
    }//GEN-LAST:event_addUserBtnActionPerformed

    private void leaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leaveBtnActionPerformed
        ArrayList<Object> par = new ArrayList<Object>();
        par.add(groupname);
        par.add(client.getUsername());
        Message result = client.sendTCP(10, par);
        switch(result.getResult()) {
            case -2:
                leave();
                break;
            case 1:
                leave();
                break;
            default:
                JOptionPane.showMessageDialog(null, "There was a problem performing this operation", "Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_leaveBtnActionPerformed

    private void leave() {
        UIWindow previous = client.removeWindow();
        previous.setVisible(true);
        this.setVisible(false);
        this.dispose();
    }
    
    public void setUser(String user) {
        pickedUser = user;
        ArrayList<Object> par = new ArrayList<Object>();
        par.add(groupname);
        par.add(pickedUser);
        Message result = client.sendTCP(9,par);
        if (result.getResult()<0)
            JOptionPane.showMessageDialog(null, result.toString(), "Error",JOptionPane.ERROR_MESSAGE);
        else
            JOptionPane.showMessageDialog(null, result.toString(), "Success",JOptionPane.INFORMATION_MESSAGE);
    }
    
    
