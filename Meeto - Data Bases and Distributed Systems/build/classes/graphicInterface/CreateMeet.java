    private void leave() {
        JFrame previous = client.removeWindow();
        previous.setVisible(true);
        this.setVisible(false);
        this.dispose();
    }
    
    private void checkNumbers(JTextField handler, int min, int max) {
        if (handler.getText().length()!=0)
            try {
                handler.setBackground(Color.white);
                int aux = Integer.parseInt(handler.getText());
                if (max!=0)
                    if ((aux>max)||(aux<min))
                        handler.setBackground(Color.red);
            }
            catch (NumberFormatException e) {
                handler.setText("");
            }
        else handler.setBackground(Color.white);
    }
    
    private boolean checkDate() {
        int mesval = Integer.parseInt(this.mes.getText());
        int diaval = Integer.parseInt(this.dia.getText());
        int anoval = Integer.parseInt(this.ano.getText());
        int hourval = Integer.parseInt(this.hour.getText());
        int minval = Integer.parseInt(this.minute.getText());
        
        if ((hourval>=24)||(hourval<0)||(minval>59)||(minval<0)) return false;
        if ((mesval>12)||(mesval<1)) return false;
        if (diaval>31) return false;
        
        if ((mesval == 4)||(mesval == 6)||(mesval == 9)|| (mesval == 11)) {
            if (diaval > 30) return false;
        }
        else if (mesval==2){
            if ((((anoval%4)==0)&&(anoval%100!=0))||(anoval%400==0)) {
                if (diaval>29) return false;
            }
            else if (diaval>28) return false;
        }
        
        return true;
    }

