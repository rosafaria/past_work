    private void OutcomeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_OutcomeKeyPressed
        if (this.description.getText().length()>Global.DESCRIPTIONLEN) {
            this.description.setText(this.description.getText().substring(0,Global.DESCRIPTIONLEN));
        }
        this.description.setText(this.description.getText().replace("\t",""));
        this.description.setText(this.description.getText().replace("\n",""));
    }//GEN-LAST:event_OutcomeKeyPressed

    private void aliasKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_aliasKeyTyped
        String aux = this.alias.getText();
        if (aux.length()>0) {
            if (!Character.isLetterOrDigit(aux.charAt(aux.length()-1))) {
                this.alias.setText(aux.substring(0, aux.length()-1));
            }
            if (aux.length()>Global.MEETINGALIASLEN) {
                this.alias.setText(aux.substring(0,Global.MEETINGALIASLEN));
            }
        }
    }//GEN-LAST:event_aliasKeyTyped

