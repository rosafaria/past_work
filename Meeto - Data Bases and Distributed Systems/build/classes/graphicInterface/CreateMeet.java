    private void OKBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKBtnActionPerformed
        if (!checkDate()) {
            JOptionPane.showMessageDialog(null, "Invalid date/time", "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (this.alias.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please choose an alias for the meeting", "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (this.title.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Invalid title", "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (this.description.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Empty desired outcome", "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (this.location.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Empty location", "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        ArrayList<Object> par = new ArrayList<Object>();
        
        par.add(this.alias.getText());
        par.add(this.title.getText());
        par.add(this.description.getText());
        String datestr = this.dia.getText()+"-"+this.mes.getText()+"-"+this.ano.getText()+" "+this.hour.getText()+":"+this.minute.getText();
        par.add(datestr);
        par.add(this.location.getText());
        Message result = client.sendTCP(15,par);
        switch(result.getResult()) {
            case -2:
                JOptionPane.showMessageDialog(null, result.toString(), "Error",JOptionPane.ERROR_MESSAGE);
                leave();
                break;
            case -1:
                JOptionPane.showMessageDialog(null, "Alias already in use", "Error",JOptionPane.ERROR_MESSAGE);
                break;
            case 1:
                JOptionPane.showMessageDialog(null, "Meeting has been created", "Success",JOptionPane.INFORMATION_MESSAGE);
                leave();
                break;
            default:
                JOptionPane.showMessageDialog(null, "There was a problem performing this operation", "Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_OKBtnActionPerformed

